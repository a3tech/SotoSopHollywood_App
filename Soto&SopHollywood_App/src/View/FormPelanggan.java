    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        p = new EntitasPelanggan();
        p.setId_pelanggan(txtIdPelanggan.getText());
        p.setNama_pelanggan(txtNamaPelanggan.getText());
        
        if(txtNamaPelanggan.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Semua Data Pelanggan Wajib Diisi !!!",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }else {
        if (proses.equalsIgnoreCase("INSERT")){
            status = pelangganDAO.insertPelanggan(p);
        if(status == true){
            JOptionPane.showMessageDialog(null, "Data Pelanggan Berhasil Disimpan",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null, "Data Pelanggan Gagal Disimpan !",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
        }else{
            status = pelangganDAO.updatePelanggan(p);
        if(status == true){
            JOptionPane.showMessageDialog(null, "Data Pelanggan Berhasil Diubah",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null, "Data Pelanggan Gagal Diubah !",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
        }
     refreshIsiTabel();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

