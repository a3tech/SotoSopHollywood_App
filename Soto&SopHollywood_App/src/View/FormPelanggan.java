    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    if (JOptionPane.showConfirmDialog(null, "Apakah Ingin Menghapus "
            + "Data Ini ?",
         "Konfirmasi", 
         JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
         status = pelangganDAO.deletePelanggan(txtIdPelanggan.getText());
            
        if (status == true){
         JOptionPane.showMessageDialog(null, "Data Pelanggan Berhasil Dihapus",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }else if (status == false){
         JOptionPane.showMessageDialog(null, "Data Pelanggan Gagal Dihapus !",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
         }
        }
        refreshIsiTabel();
    }//GEN-LAST:event_btnDeleteActionPerformed

