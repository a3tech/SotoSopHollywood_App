    private void aturButton (boolean status){
        btnAdd.setEnabled(status);
        btnEdit.setEnabled(status);
        btnSave.setEnabled(!status);
        btnDelete.setEnabled(status);
        btnSearch.setEnabled(status);
        btnRefresh.setEnabled(status);
        if(status == true){
            btnExit.setText("Keluar");
        }else{
            btnExit.setText("Batal");
        }
    }
    private void aturTeks (boolean status){
        txtNamaPelanggan.setEditable(status);
        txtCari.setEditable(!status);
    }
    
    private void refreshIsiTabel(){
        listPelanggan = pelangganDAO.selectPelanggan(txtCari.getText(),
                txtCari.getText());
        dtplgn = (DefaultTableModel) tblPelanggan.getModel();
        dtplgn.setRowCount(0);
        
        for (EntitasPelanggan data : listPelanggan){
            dtplgn.addRow(new Object[]{data.getId_pelanggan(), 
                data.getNama_pelanggan(),data.getNo_telp()});
            }
        if (tblPelanggan.getRowCount() > 0){
        baris = tblPelanggan.getRowCount() - 1;
        tblPelanggan.setRowSelectionInterval(baris, baris);
        aturButton(true);
        aturTeks(false);
        proses = "";
        }
    }    
    

        
