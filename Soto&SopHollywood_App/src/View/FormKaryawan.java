    private void btnSave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave1ActionPerformed
        k = new EntitasUser();
        k.setId_user(txtIdkaryawan.getText());
        k.setNama_user(txtNamaKaryawan.getText());
        k.setUsername(txtUsername.getText());
        k.setPassword(txtPasswordKaryawan.getText());
        k.setJabatan(txtJabatan.getText());
         if(txtNamaKaryawan.getText().equals("") || txtPasswordKaryawan.getText().equals("") ||
                 txtUsername.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Semua Data Karyawan Wajib Diisi !!!",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }else {
        if (proses.equalsIgnoreCase("INSERT")){
            status = userDAO.insertUser(k);
            
             if(status==true){
        JOptionPane.showMessageDialog(null, "Data Karyawan Berhasil Disimpan",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }else if(status == false){
            JOptionPane.showMessageDialog(null, "Data Karyawan Gagal Disimpan !",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
  
        }else{
        status = userDAO.updateUser(k);
         if(status==true){
        JOptionPane.showMessageDialog(null, "Data Karyawan Berhasil Diubah",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }else if(status == false){
            JOptionPane.showMessageDialog(null, "Data Karyawan Gagal Diubah!",
                    "Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
        }

      
        refreshIsiTabel();
         }
    }//GEN-LAST:event_btnSave1ActionPerformed

