    private void refreshIsiTabel(){
    listUser = userDAO.selectUser(txtCari1.getText(),
                txtCari1.getText());
        dtuser = (DefaultTableModel) TblUser.getModel();
        dtuser.setRowCount(0);
        
        for (EntitasUser data : listUser){
            dtuser.addRow(new Object[]{data.getId_user(), 
             data.getNama_user(),data.getUsername(), data.getPassword(), data.getJabatan()});}
        if (TblUser.getRowCount() > 0){
        baris = TblUser.getRowCount() - 1;
        TblUser.setRowSelectionInterval(baris, baris);
        aturButton(true);
        aturTeks(false);
        proses = "";
        }
    }   
